class ExemplosFor
{
    ExemplosFor()
    {
        // Ciclo For - Exemplos Simples
        // Primeira parte: declaração e inicialização da variável do ciclo. Costuma se usar um inteiro para contabilizar as instruções.
        // Segunda parte: Condição do ciclo (Pergunta: 0 é menor que 13?)
        // Terceira parte: Incremento ou decremento da variável do ciclo.
        for( int i = 0; i <= 13; i++ )
        {
            System.out.println("Escreve texto!!");
            System.out.println(i);
        }
        
        for( int a = 2; a < 8; a += 2 )
        {
            System.out.println("Escreve texto!!");
            System.out.println(a);
            
        }
    }
}
